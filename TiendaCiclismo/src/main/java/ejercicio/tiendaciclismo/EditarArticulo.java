    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try{
            DefaultTableModel model=(DefaultTableModel)jTable1.getModel();
            int fila=jTable1.getSelectedRow();
            if (fila >= 0) {
            registro.modificarArticulo(registro.getArticulos().get(fila).getCodigo(), registro.getArticulos().get(fila).getCodigoTipoProducto(), jTextField1.getText(), jComboBox2.getSelectedItem().toString(), Integer.parseInt(jComboBox3.getSelectedItem().toString()), jTextField3.getText(), (int) Double.parseDouble(jTextField4.getText()), Integer.parseInt(jTextField5.getText()));
            model.setValueAt(jTextField1.getText(),fila , 2);
            model.setValueAt(jComboBox2.getSelectedItem().toString(), fila, 3);
            model.setValueAt(Integer.valueOf(jComboBox3.getSelectedItem().toString()), fila, 4);
            model.setValueAt(jTextField3.getText(), fila, 5);
            model.setValueAt(Double.valueOf(jTextField4.getText()), fila, 6);
            model.setValueAt(Integer.valueOf(jTextField5.getText()), fila, 7);
            }
            
            jComboBox1.setEnabled(true);
            jTextField1.setText("");
            jComboBox1.setSelectedIndex(0);
            jComboBox2.setSelectedIndex(0);
            jComboBox3.setSelectedIndex(0);
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
            iniciarTabla();
            
        }catch (Exception e){
            jComboBox1.setEnabled(true);
            jComboBox1.setEnabled(true);
            jTextField1.setText("");
            jComboBox1.setSelectedIndex(0);
            jComboBox2.setSelectedIndex(0);
            jComboBox3.setSelectedIndex(0);
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
            JOptionPane.showMessageDialog(jPanel1, "Debe seleccionar una fila.");
            System.err.println("Error al leer: " + e.getMessage());
            
        
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (confirmacion()){
            try{
                DefaultTableModel model=(DefaultTableModel)jTable1.getModel();
                int filaSeleccionada=jTable1.getSelectedRow();
                registro.deleteArticulo(registro.getArticulos().get(filaSeleccionada).getCodigo(),registro.getArticulos().get(filaSeleccionada).getCodigoTipoProducto() );
                jComboBox1.setEnabled(true);
                jTextField1.setText("");
                jComboBox1.setSelectedIndex(0);
                jComboBox2.setSelectedIndex(0);
                jComboBox3.setSelectedIndex(0);
                jTextField3.setText("");
                jTextField4.setText("");
                jTextField5.setText("");
                model.removeRow(filaSeleccionada);
                iniciarTabla();
                jComboBox1.setEnabled(true);
            }catch (Exception e){
                jComboBox1.setEnabled(true);
                JOptionPane.showMessageDialog(jPanel1, "Debe seleccionar una fila.");
            }
        }else{
            JOptionPane.showMessageDialog(jPanel1, "No se elimino el producto.");
        }
    
    }//GEN-LAST:event_jButton3ActionPerformed

    private boolean confirmacion(){
        int opcion = JOptionPane.showConfirmDialog(jPanel1,
                    "¿Desea realizar la acción?",
                    "Confirmación",
                    JOptionPane.OK_CANCEL_OPTION);

            if (opcion == JOptionPane.OK_OPTION) {
                return true;
            } else {
                return false;
            }
    }
    
    
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (!jTextField1.getText().isEmpty() && !jTextField3.getText().isEmpty() && !jTextField4.getText().isEmpty() && !jTextField5.getText().isEmpty()){
            for (Articulo art:registro.getArticulos()){
                String nombreGuardado=art.getNombre();
                if (nombreGuardado.equalsIgnoreCase(jTextField1.getText())){
                    jComboBox1.setEnabled(true);
                    jTextField1.setText("");
                    jComboBox1.setSelectedIndex(0);
                    jComboBox2.setSelectedIndex(0);
                    jComboBox3.setSelectedIndex(0);
                    jTextField3.setText("");
                    jTextField4.setText("");
                    jTextField5.setText("");
                    JOptionPane.showMessageDialog(jPanel1, "Debe agregar un articulo que no tenga ese nombre.");
                    return;
                }
            }
            
            int codigoProducto= Integer.parseInt(jComboBox1.getSelectedItem().toString());
            int tamano= Integer.parseInt(jComboBox3.getSelectedItem().toString());
            int precio=Integer.parseInt(jTextField4.getText());
            int cantidad=Integer.parseInt(jTextField5.getText());
            
            registro.agregarArticulo(codigoProducto, jTextField1.getText() , jComboBox2.getSelectedItem().toString(), tamano, jTextField3.getText(), precio , cantidad);
            iniciarTabla();
            jComboBox1.setEnabled(true);
            jTextField1.setText("");
            jComboBox1.setSelectedIndex(0);
            jComboBox2.setSelectedIndex(0);
            jComboBox3.setSelectedIndex(0);
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
            
        }else{
            JOptionPane.showMessageDialog(jPanel1, "Debe rellenar los espacios.");
        } 
    }//GEN-LAST:event_jButton1ActionPerformed

