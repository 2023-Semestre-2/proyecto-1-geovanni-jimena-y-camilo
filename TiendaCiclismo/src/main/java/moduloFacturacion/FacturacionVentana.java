    /**
     * Método invocado al presionar el botón "Buscar".
     * Realiza una búsqueda de facturas según los criterios especificados en la interfaz.
     * 
     * @param evt Evento de acción.
     */
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{    
            ArrayList<Factura> facturasBuscadas = new ArrayList<>();
            if(jfecha.getDate()!=null){
                DateFormat formato=new SimpleDateFormat(jfecha.getDateFormatString());
                String fecha= formato.format(jfecha.getDate());
                facturasBuscadas=registro.buscarFecha(fecha);
            }
            if(!jTextField2.getText().isEmpty()){
                facturasBuscadas.addAll(registro.buscarCliente(jTextField2.getText()));
            }
            if(!jTextField1.getText().isEmpty()){
                facturasBuscadas.addAll(registro.buscarNumero(Integer.parseInt(jTextField1.getText())));
            }   
            if(jfecha.getDate()==null && jTextField1.getText().isEmpty() && jTextField2.getText().isEmpty()){
                IniciarTabla();
                return;
            }
            
            seleccionaFacturas(facturasBuscadas);
            
        }catch (NumberFormatException e){
            JOptionPane.showMessageDialog(jPanel1, "Debe ingresar el formato correcto del numero de factura.");
        }catch (Exception e){
            JOptionPane.showMessageDialog(jPanel1, "Debe ingresar el formato correcto en la fecha");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

