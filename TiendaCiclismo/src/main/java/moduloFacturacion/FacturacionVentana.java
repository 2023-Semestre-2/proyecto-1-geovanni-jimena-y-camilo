    /**
     * Método para mostrar las facturas seleccionadas en la tabla.
     * 
     * @param facturas Lista de facturas a mostrar.
     */
    private void seleccionaFacturas(ArrayList<Factura> facturas){
        tablaFactura.setRowCount(0);
        
        //Verificar que el Arraylist no tenga articulos repetidos
        ArrayList<Factura> listaSinDuplicados = new ArrayList<>();
        for (Factura elemento : facturas) {
            if (!listaSinDuplicados.contains(elemento)) {
                listaSinDuplicados.add(elemento);
            }
        }
        
        //Agrega las facturas buscadas
        for (Factura factura:listaSinDuplicados){
            Object[] fila={factura.getNumeroFactura(),factura.getNombre(),factura.getFecha(),factura.getEstado()};
            tablaFactura.addRow(fila);
        }
        pack();
        setLocationRelativeTo(null);
        setVisible(true);
    }
    
    /**
     * Método para inicializar la tabla con todas las facturas.
     */
    public void IniciarTabla(){
        tablaFactura.setRowCount(0);
        //Datos de las facturas
        for (Factura factura:registro.getFacturas()){
            Object[] fila={factura.getNumeroFactura(),factura.getNombre(),factura.getFecha(),factura.getEstado()};
            tablaFactura.addRow(fila);
        }
        
        pack();
        setLocationRelativeTo(null);
        setVisible(true);
        
    }
    
    
