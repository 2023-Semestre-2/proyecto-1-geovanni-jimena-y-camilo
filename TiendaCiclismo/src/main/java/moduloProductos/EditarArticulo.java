    /**
     * Método que maneja el evento de clic en el botón "Modificar". Obtiene los
     * datos ingresados por el usuario y realiza la modificación del artículo
     * seleccionado en la tabla y en la fuente de datos.
     *
     * @param evt Evento de acción generado por el botón "Modificar".
     */
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try{
            if (!jTextField1.getText().isEmpty() && !jTextField3.getText().isEmpty() && !jTextField4.getText().isEmpty() && !jTextField5.getText().isEmpty()){ 
                DefaultTableModel model=(DefaultTableModel)jTable1.getModel();
                int fila=jTable1.getSelectedRow();
                if (fila >= 0) {
                registro.modificarArticulo(registro.getArticulos().get(fila).getCodigo(), registro.getArticulos().get(fila).getCodigoTipoProducto(), jTextField1.getText(), jComboBox2.getSelectedItem().toString(), Integer.parseInt(jComboBox3.getSelectedItem().toString()), jTextField3.getText(), (int) Double.parseDouble(jTextField4.getText()), Integer.parseInt(jTextField5.getText()));
                
                model.setValueAt(jTextField1.getText(),fila , 2);
                model.setValueAt(jComboBox2.getSelectedItem().toString(), fila, 3);
                model.setValueAt(Integer.valueOf(jComboBox3.getSelectedItem().toString()), fila, 4);
                model.setValueAt(jTextField3.getText(), fila, 5);
                model.setValueAt(Double.valueOf(jTextField4.getText()), fila, 6);
                model.setValueAt(Integer.valueOf(jTextField5.getText()), fila, 7);
                JOptionPane.showMessageDialog(null, "Se ha modificado el articulo", "Éxito", JOptionPane.INFORMATION_MESSAGE);
                jButton2.setEnabled(false);
                jButton3.setEnabled(false);
                }
            }else{
                JOptionPane.showMessageDialog(jPanel1, "Debe rellenar los espacios.");
                return;
            }
            
            limpiarCampos();
            iniciarTabla();
            
        }catch (Exception e){
            limpiarCampos();
            JOptionPane.showMessageDialog(jPanel1, "Debe seleccionar una fila.");
        }
    }//GEN-LAST:event_jButton2ActionPerformed
    
    
    /**
     * Método que maneja el evento de clic en el botón "Eliminar". Verifica si
     * se puede eliminar el artículo seleccionado y realiza la eliminación si es
     * posible.
     *
     * @param evt Evento de acción generado por el botón "Eliminar".
     */
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (confirmacion()){
            try{
                DefaultTableModel model=(DefaultTableModel)jTable1.getModel();
                int filaSeleccionada=jTable1.getSelectedRow();
                
                if (!articuloFacturado(registro.getArticulos().get(filaSeleccionada).getCodigo(),registro.getArticulos().get(filaSeleccionada).getCodigoTipoProducto() )){
                    registro.deleteArticulo(registro.getArticulos().get(filaSeleccionada).getCodigo(),registro.getArticulos().get(filaSeleccionada).getCodigoTipoProducto() );
                    limpiarCampos();
                    model.removeRow(filaSeleccionada);
                    iniciarTabla();
                    JOptionPane.showMessageDialog(null, "Se ha eliminado el articulo", "Éxito", JOptionPane.INFORMATION_MESSAGE);
                    jComboBox1.setEnabled(true);
                    jButton2.setEnabled(false);
                    jButton3.setEnabled(false);
                }else{
                    JOptionPane.showMessageDialog(jPanel1, "El artículo no puede ser eliminado porque se encuentra facturado");
                }
            }catch (Exception e){
                jComboBox1.setEnabled(true);
                JOptionPane.showMessageDialog(jPanel1, "Debe seleccionar una fila.");
            }
        }else{
            JOptionPane.showMessageDialog(jPanel1, "No se elimino el producto.");
        }
    
    }//GEN-LAST:event_jButton3ActionPerformed

    
    /**
     * Método que muestra un cuadro de diálogo de confirmación y devuelve true
     * si el usuario elige realizar la acción, o false si cancela.
     *
     * @return true si el usuario confirma, false si cancela.
     */
    private boolean confirmacion(){
        int opcion = JOptionPane.showConfirmDialog(jPanel1,
                    "¿Desea realizar la acción?",
                    "Confirmación",
                    JOptionPane.OK_CANCEL_OPTION);

            if (opcion == JOptionPane.OK_OPTION) {
                return true;
            } else {
                return false;
            }
    }
    
    
    /**
     * Método que verifica si un artículo está asociado a alguna factura en el
     * sistema.
     *
     * @param codigo Código del artículo.
     * @param codigoProducto Código del producto al que pertenece el artículo.
     * @return true si el artículo está facturado, false si no lo está.
     */
    private boolean articuloFacturado(int codigo,int codigoProducto){
        RegistroFacturacion registroFacturas=new RegistroFacturacion();
        registroFacturas.FacturasArchivo(reg.leer("Facturas"));
        
        for(Factura factura:registroFacturas.getFacturas()){
            int codigoExiste= factura.getCodigoArticulo();
            int codigoProductoExiste=factura.getCodigoProducto();
            if (codigoExiste==codigo && codigoProductoExiste==codigoProducto){
                return true;
            }
        }
        return false;
    }
    
    /**
     * Método que maneja el evento de clic en el botón "Agregar". Agrega un
     * nuevo artículo con los datos proporcionados por el usuario.
     *
     * @param evt Evento de acción generado por el botón "Agregar".
     */
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (!jTextField1.getText().trim().isEmpty() && !jTextField3.getText().isEmpty() && !jTextField4.getText().trim().isEmpty() && !jTextField5.getText().isEmpty()){
            for (Articulo art:registro.getArticulos()){
                String nombreGuardado=art.getNombre();
                if (nombreGuardado.equalsIgnoreCase(jTextField1.getText())){
                    limpiarCampos();
                    JOptionPane.showMessageDialog(jPanel1, "Debe agregar un articulo que no tenga ese nombre.");
                    return;
                }
            }
            
            
            
            // Ahora se busca el codigo con el nombre del producto elegido
            int codigoProducto=registro.codigoProducto(jComboBox1.getSelectedItem().toString());
            
            
            //Se cambian los demas atributos
            int tamano= Integer.parseInt(jComboBox3.getSelectedItem().toString());
            int precio=Integer.parseInt(jTextField4.getText());
            int cantidad=Integer.parseInt(jTextField5.getText());
            
            registro.agregarArticulo(codigoProducto, jTextField1.getText() , jComboBox2.getSelectedItem().toString(), tamano, jTextField3.getText(), precio , cantidad);
            
            iniciarTabla();
            JOptionPane.showMessageDialog(null, "Se ha agregado el articulo", "Éxito", JOptionPane.INFORMATION_MESSAGE);
            jButton2.setEnabled(false);
            jButton3.setEnabled(false);
            limpiarCampos();
            
        }else{
            JOptionPane.showMessageDialog(jPanel1, "Debe rellenar los espacios con el formato correcto.");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed
    
    
